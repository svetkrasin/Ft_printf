#include <sys/_types/_size_t.h>

size_t	ft_num_of_digs(long n, int base)
{
	size_t l;

	l = n == 0 ? 1 : 0;
	while (n != 0)
	{
		n /= base;
		++l;
	}
	return (l);
}
