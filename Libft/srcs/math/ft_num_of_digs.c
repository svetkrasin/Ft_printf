#include <_types/_intmax_t.h>

unsigned int	ft_num_of_digs(intmax_t n, int base)
{
	unsigned int l;

	l = n == 0 ? 1 : 0;
	while (n != 0)
	{
		n /= base;
		++l;
	}
	return (l);
}
